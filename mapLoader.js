function addMarkers(map){
	let homeMarker = new google.maps.Marker({
		position:{lat: 47.561, lng: -122.221},
		map:map
	});
	let homeInfo = new google.maps.InfoWindow({
		content: "<h5>My Home: Mercer Island</h5>"
	});
	homeMarker.addListener('click', function(){
		homeInfo.open(map, homeMarker);
	});

	let collegeMarker = new google.maps.Marker({
		position:{lat: 47.668, lng: -117.402},
		map:map
	});
	let collegeInfo = new google.maps.InfoWindow({
		content: "<h5>My College: Gonzaga University</h5>"
	});
	collegeMarker.addListener('click', function(){
		collegeInfo.open(map, collegeMarker);
	});

	let workMarker = new google.maps.Marker({
		position:{lat: 39.5856, lng: -104.884},
		map:map
	});
	let workInfo = new google.maps.InfoWindow({
		content: "<h5>My Employer: FAST Enterprises</h5>"
	});
	workMarker.addListener('click', function(){
		workInfo.open(map, workMarker);
	});
}